package com.index;
import java.util.*;
public class Operations {
	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		System.out.println("Enter num1");
		int num1=s.nextInt();
		System.out.println("Enter num2");
		int num2=s.nextInt();
		
		System.out.println("Addition of 2 number is:"+(num1+num2));
		System.out.println("Subraction of 2 number is:"+(num1-num2));
		System.out.println("Multiplication of 2 number is:"+(num1*num2));
		System.out.println("Division of 2 number is:"+(num1/num2));
		System.out.println("Remainder of 2 number is:"+(num1%num2));
		
		
	}

}
